import json

file = open("cars.json", "r")
cars = json.load(file)
file.close()


class Cars:
    def __init__(self,
                 id=None,
                 name=None,
                 model=None,
                 year=None):
        self.id = id
        self.name = name
        self.model = model
        self.year = year

    def new_cars(self, limit_count):
        return sorted(cars , key=lambda d : d["year"])[-limit_count:]

    def search(self):
        res = []
        for car in cars:
            if car["name"] == self.name:
                res.append(car)
        return res

    def filter_year(self, from_year , to_year):
        res = []
        for car in cars:
            if from_year <= car.get("year") <= to_year:
                res.append(car)
        return res

print(*sorted(Cars().filter_year(2005, 2006), key=lambda x : x['year']), sep='\n')





